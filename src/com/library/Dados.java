package library;

public class Dados {
	private int idUsuario;
	private int idLivro;
	
	public Dados(int idUsuario, int idLivro){
		this.idUsuario = idUsuario;
		this.idLivro = idLivro;
	}
	
	
	public int getIdUsuario() {
		return idUsuario;
	}

	public int getIdLivro() {
		return idLivro;
	}
	
	
}
